package com.accenture.bby.sdp.web.beans;

/**
 * @author Ross Stockman
 *
 */
public class XmlAnalysis {
	private String id;
	private String currentXml;
	private String updatedXml;
	private String responseXml;
	private String changeAnalysis;
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the currentXml
	 */
	public String getCurrentXml() {
		return currentXml;
	}
	/**
	 * @param currentXml the currentXml to set
	 */
	public void setCurrentXml(String currentXml) {
		this.currentXml = currentXml;
	}
	/**
	 * @return the updatedXml
	 */
	public String getUpdatedXml() {
		return updatedXml;
	}
	/**
	 * @param updatedXml the updatedXml to set
	 */
	public void setUpdatedXml(String updatedXml) {
		this.updatedXml = updatedXml;
	}
	/**
	 * @return the responseXml
	 */
	public String getResponseXml() {
		return responseXml;
	}
	/**
	 * @param responseXml the responseXml to set
	 */
	public void setResponseXml(String responseXml) {
		this.responseXml = responseXml;
	}
	/**
	 * @return the changeAnalysis
	 */
	public String getChangeAnalysis() {
		return changeAnalysis;
	}
	/**
	 * @param changeAnalysis the changeAnalysis to set
	 */
	public void setChangeAnalysis(String changeAnalysis) {
		this.changeAnalysis = changeAnalysis;
	}
}
