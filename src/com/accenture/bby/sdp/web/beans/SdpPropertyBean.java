package com.accenture.bby.sdp.web.beans;

public class SdpPropertyBean {
	private String mPropertyName;
	private String mPropertyValue;

	public SdpPropertyBean() {
	}

	public String getPropertyName() {
		return mPropertyName;
	}

	public void setPropertyName(final String pPropertyName) {
		mPropertyName = pPropertyName;
	}

	public String getPropertyValue() {
		return mPropertyValue;
	}

	public void setPropertyValue(final String pPropertyValue) {
		mPropertyValue = pPropertyValue;
	}
}
